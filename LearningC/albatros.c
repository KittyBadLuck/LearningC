/* Print poem "L'Albatros" by Baudelaire
Lou de Tarade
20/06/2024*/

#include<stdio.h>
int main(void)
{
    printf("Souvent, pour s’amuser, les hommes d’equipage\n"
        "Prennent des albatros, vastes oiseaux des mers,\n" 
        "Qui suivent, indolents compagnons de voyage, \n"
        "Le navire glissant sur les gouffres amers.\n\n");

    printf("A peine les ont-ils deposes sur les planches,\n"
        "Que ces rois de l'azur, maladroits et honteux,\n"
        "Laissent piteusement leurs grandes ailes blanches \n"
        "Comme des avirons trainer a cote d'eux.\n\n");

    printf("Ce voyageur aile, comme il est gauche et veule !\n"
        "Lui, naguere si beau, qu'il est comique et laid !\n"
        "L'un agace son bec avec un brule-gueule, \n"
        "L'autre mime, en boitant, l'infirme qui volait !\n\n");

    printf("Le Poete est semblable au prince des nuees\n"
        "Qui hante la tempete et se rit de l'archer ;\n"
        "Exile sur le sol au milieu des huees, \n"
        "Ses ailes de geant l'empechent de marcher.\n\n");

    return 0;
}

